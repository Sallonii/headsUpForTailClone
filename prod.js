//appending each item of product 
function createAndAppendToys(eachItem) {
    let {
        img,
        name,
        rating,
        price
    } = eachItem;
    console.log(img);

    //bootstrap grid container
    let divCont = document.createElement("div");
    divCont.classList.add("col-6", "col-md-4");
    dogToyCategoriesEl.appendChild(divCont);

    //image element
    let imgEl = document.createElement("img");
    imgEl.src = img;
    imgEl.classList.add("w-100", "m-3", "img-style")
    divCont.appendChild(imgEl);

    //nameEl
    let nameEl = document.createElement("h6");
    nameEl.textContent = name;
    nameEl.classList.add("dog-toy-name-style");
    divCont.appendChild(nameEl);

    //rating Element
    let ratingEl = document.createElement("p");
    ratingEl.textContent = rating;
    ratingEl.classList.add("dog-toy-name-style")
    divCont.appendChild(ratingEl);

    //price element
    let priceEl = document.createElement("p");
    priceEl.textContent = "₹" + price;
    priceEl.classList.add("dog-toy-name-style")
    divCont.appendChild(priceEl);
}

function fetchHardcodedData() {
    return new Promise((resolve) => {
      setTimeout(() => {
        resolve({
          json: () => Promise.resolve(hardcodedJSON),
        });
      }, 100);
    });
  }

//Products data of Dog
function getData(item) {

    for (let eachItem of item) {
        createAndAppendToys(eachItem);
    }
}

function getDogToysList() {
    dogToyCategoriesEl.textContent = "";

    fetchHardcodedData()
        .then(response => response.json())
        .then(data => {
            getData(data.dogs.toys);
        })
        .catch(error => console.error('Error:', error));
}


